package com.photo.photography.duplicatephotos.pixelsrelated;

public class RGBObject {
    public int blue;
    public int green;
    public int reb;

    public int getReb() {
        return this.reb;
    }

    public void setReb(int i) {
        this.reb = i;
    }

    public int getGreen() {
        return this.green;
    }

    public void setGreen(int i) {
        this.green = i;
    }

    public int getBlue() {
        return this.blue;
    }

    public void setBlue(int i) {
        this.blue = i;
    }
}
